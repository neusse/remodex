package com.remodex.mobile.data

import com.remodex.mobile.core.model.CODEX_PAIRING_QR_VERSION
import com.remodex.mobile.core.model.CODEX_SECURE_CLOCK_SKEW_TOLERANCE_SECONDS
import com.remodex.mobile.core.model.CodexPairingQRPayload
import com.remodex.mobile.core.transport.validateRelayUrl
import kotlinx.coroutines.Dispatchers
import kotlinx.coroutines.withContext
import kotlinx.serialization.Serializable
import kotlinx.serialization.encodeToString
import kotlinx.serialization.json.Json
import kotlinx.serialization.json.jsonObject
import okhttp3.MediaType.Companion.toMediaType
import okhttp3.OkHttpClient
import okhttp3.Request
import okhttp3.RequestBody.Companion.toRequestBody
import okhttp3.HttpUrl
import java.util.Base64

private val pairingJson =
    Json {
        ignoreUnknownKeys = true
        coerceInputValues = true
    }

private const val QR_SCANNER_BRIDGE_UPDATE_COMMAND = "npm install -g remodex@latest"
private val pairingJsonMediaType = "application/json; charset=utf-8".toMediaType()
private const val MAX_PAIRING_FIELD_LENGTH = 256
private const val ED25519_PUBLIC_KEY_BYTES = 32
private val pairingIdRegex = Regex("^[A-Za-z0-9._:-]{1,$MAX_PAIRING_FIELD_LENGTH}$")

@Serializable
private data class PairingCodeResolveRequest(
    val code: String,
)

@Serializable
private data class PairingCodeResolveResponse(
    val ok: Boolean = false,
    val v: Int? = null,
    val sessionId: String? = null,
    val macDeviceId: String? = null,
    val macIdentityPublicKey: String? = null,
    val expiresAt: Long? = null,
    val error: String? = null,
    val code: String? = null,
)

sealed class QrPairingValidationResult {
    data class Success(
        val payload: CodexPairingQRPayload,
    ) : QrPairingValidationResult()

    data class ScanError(
        val message: String,
    ) : QrPairingValidationResult()

    data class BridgeUpdateRequired(
        val title: String,
        val message: String,
        val command: String = QR_SCANNER_BRIDGE_UPDATE_COMMAND,
    ) : QrPairingValidationResult()
}

/**
 * Classifies scanned QR payloads (parity with [QRScannerPairingValidator.swift](CodexMobile/CodexMobile/Views/QRScannerPairingValidator.swift)).
 */
fun validatePairingQrCode(
    code: String,
    nowEpochMillis: Long = System.currentTimeMillis(),
): QrPairingValidationResult {
    val trimmed = code.trim()
    val payload =
        runCatching {
            pairingJson.decodeFromString<CodexPairingQRPayload>(trimmed)
        }.getOrNull()

    if (payload != null) {
        if (payload.v != CODEX_PAIRING_QR_VERSION) {
            return QrPairingValidationResult.BridgeUpdateRequired(
                title = "Update Remodex on your Mac before scanning",
                message =
                    "This QR code was generated by a different Remodex npm version. " +
                        "Update the package on your Mac to the latest release before scanning a new QR code.",
            )
        }
        if (payload.relay.trim().isEmpty()) {
            return QrPairingValidationResult.ScanError(
                "QR code is missing the relay URL. Re-generate the code from the bridge.",
            )
        }
        if (payload.sessionId.trim().isEmpty()) {
            return QrPairingValidationResult.ScanError(
                "QR code is missing the session ID. Re-generate the code from the bridge.",
            )
        }
        validatePairingFields(
            relay = payload.relay,
            sessionId = payload.sessionId,
            macDeviceId = payload.macDeviceId,
            macIdentityPublicKey = payload.macIdentityPublicKey,
        )?.let { return it }
        val skewMillis = (CODEX_SECURE_CLOCK_SKEW_TOLERANCE_SECONDS * 1000).toLong()
        if (nowEpochMillis > payload.expiresAt + skewMillis) {
            return QrPairingValidationResult.ScanError(
                "This pairing QR code has expired. Generate a new one from the Mac bridge.",
            )
        }
        return QrPairingValidationResult.Success(payload)
    }

    if (looksLikeRemodexPairingPayload(trimmed)) {
        return QrPairingValidationResult.BridgeUpdateRequired(
            title = "Update Remodex on your Mac before scanning",
            message =
                "This QR code looks like it came from an older Remodex bridge. " +
                    "Update the npm package on your Mac to the latest release before scanning a new QR code.",
        )
    }

    return QrPairingValidationResult.ScanError(
        "Not a valid secure pairing code. Make sure you're scanning a QR from the latest Remodex bridge.",
    )
}

suspend fun resolvePairingCode(
    httpClient: OkHttpClient,
    relayUrl: String,
    code: String,
    nowEpochMillis: Long = System.currentTimeMillis(),
): QrPairingValidationResult =
    withContext(Dispatchers.IO) {
        val trimmedCode = code.trim()
        if (trimmedCode.isEmpty()) {
            return@withContext QrPairingValidationResult.ScanError("Enter the pairing code from your desktop.")
        }

        val relayRoot =
            relayHttpRootUrl(relayUrl)
                ?: return@withContext QrPairingValidationResult.ScanError(
                    "Enter the relay URL shown on your desktop, for example ws://192.168.1.5:9000/relay.",
                )
        val endpoint = relayRoot.newBuilder().encodedPath("/v1/pairing/code/resolve").build()
        val body =
            pairingJson
                .encodeToString(PairingCodeResolveRequest(trimmedCode))
                .toRequestBody(pairingJsonMediaType)
        val request =
            Request.Builder()
                .url(endpoint)
                .post(body)
                .build()

        runCatching {
            httpClient.newCall(request).execute().use { response ->
                val responseBody = response.body?.string().orEmpty()
                val decoded =
                    runCatching {
                        pairingJson.decodeFromString<PairingCodeResolveResponse>(responseBody)
                    }.getOrNull()

                if (!response.isSuccessful || decoded?.ok != true) {
                    return@withContext QrPairingValidationResult.ScanError(
                        decoded?.error ?: "Pairing code unavailable. Check the code and relay URL.",
                    )
                }

                val version = decoded.v
                if (version != CODEX_PAIRING_QR_VERSION) {
                    return@withContext QrPairingValidationResult.BridgeUpdateRequired(
                        title = "Update Remodex on your desktop before pairing",
                        message =
                            "This pairing code was generated by a different Remodex bridge version. " +
                                "Update the desktop bridge and generate a new code.",
                    )
                }

                val sessionId = decoded.sessionId?.trim().orEmpty()
                val macDeviceId = decoded.macDeviceId?.trim().orEmpty()
                val macIdentityPublicKey = decoded.macIdentityPublicKey?.trim().orEmpty()
                val expiresAt = decoded.expiresAt
                if (
                    sessionId.isEmpty() ||
                    macDeviceId.isEmpty() ||
                    macIdentityPublicKey.isEmpty() ||
                    expiresAt == null
                ) {
                    return@withContext QrPairingValidationResult.ScanError(
                        "The relay returned incomplete pairing metadata. Generate a new code from the desktop bridge.",
                    )
                }
                val resolvedRelay = relayPayloadUrl(relayRoot)
                validatePairingFields(
                    relay = resolvedRelay,
                    sessionId = sessionId,
                    macDeviceId = macDeviceId,
                    macIdentityPublicKey = macIdentityPublicKey,
                )?.let { return@withContext it }

                val skewMillis = (CODEX_SECURE_CLOCK_SKEW_TOLERANCE_SECONDS * 1000).toLong()
                if (nowEpochMillis > expiresAt + skewMillis) {
                    return@withContext QrPairingValidationResult.ScanError(
                        "This pairing code has expired. Generate a new one from the desktop bridge.",
                    )
                }

                QrPairingValidationResult.Success(
                    CodexPairingQRPayload(
                        v = version,
                        relay = resolvedRelay,
                        sessionId = sessionId,
                        macDeviceId = macDeviceId,
                        macIdentityPublicKey = macIdentityPublicKey,
                        expiresAt = expiresAt,
                    ),
                )
            }
        }.getOrElse { error ->
            QrPairingValidationResult.ScanError(
                error.message ?: "Could not reach the relay. Check that your phone and desktop are on the same network.",
            )
        }
    }

private fun looksLikeRemodexPairingPayload(raw: String): Boolean {
    val obj =
        runCatching {
            pairingJson.parseToJsonElement(raw).jsonObject
        }.getOrNull() ?: return false
    val pairingKeys =
        setOf(
            "relay",
            "sessionId",
            "macDeviceId",
            "macIdentityPublicKey",
            "expiresAt",
            "v",
        )
    return obj.keys.any { it in pairingKeys }
}

private fun validatePairingFields(
    relay: String,
    sessionId: String,
    macDeviceId: String,
    macIdentityPublicKey: String,
): QrPairingValidationResult.ScanError? {
    val fields = listOf(relay, sessionId, macDeviceId, macIdentityPublicKey)
    if (fields.any { it.length > MAX_PAIRING_FIELD_LENGTH || containsUnsafePairingChars(it) }) {
        return QrPairingValidationResult.ScanError(
            "Pairing metadata has invalid characters or is too large. Generate a new code from the desktop bridge.",
        )
    }
    if (!pairingIdRegex.matches(sessionId) || !pairingIdRegex.matches(macDeviceId)) {
        return QrPairingValidationResult.ScanError(
            "Pairing metadata has an invalid identifier. Generate a new code from the desktop bridge.",
        )
    }
    val keyBytes =
        runCatching { Base64.getDecoder().decode(macIdentityPublicKey) }.getOrNull()
            ?: return QrPairingValidationResult.ScanError(
                "Pairing metadata has an invalid Mac identity key. Generate a new code from the desktop bridge.",
            )
    if (keyBytes.size != ED25519_PUBLIC_KEY_BYTES) {
        return QrPairingValidationResult.ScanError(
            "Pairing metadata has an invalid Mac identity key. Generate a new code from the desktop bridge.",
        )
    }
    if (validateRelayUrl(relay, requireWebSocketScheme = true) == null) {
        return QrPairingValidationResult.ScanError(
            "Pairing metadata has an invalid or insecure relay URL. Use a local relay or a secure wss:// relay.",
        )
    }
    return null
}

private fun containsUnsafePairingChars(value: String): Boolean =
    value.any { it.isISOControl() || it.isWhitespace() }

private fun relayHttpRootUrl(rawRelayUrl: String): HttpUrl? {
    val validation = validateRelayUrl(rawRelayUrl) ?: return null
    return validation.httpUrl.newBuilder()
        .encodedPath("/")
        .query(null)
        .fragment(null)
        .build()
}

private fun relayPayloadUrl(relayRoot: HttpUrl): String {
    val relayHttpUrl = relayRoot.newBuilder().encodedPath("/relay").build().toString()
    return when {
        relayHttpUrl.startsWith("https://", ignoreCase = true) -> "wss://${relayHttpUrl.substring(8)}"
        relayHttpUrl.startsWith("http://", ignoreCase = true) -> "ws://${relayHttpUrl.substring(7)}"
        else -> relayHttpUrl
    }
}
